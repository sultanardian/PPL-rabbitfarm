    {
        $offer = new Offer([
            'order_id' => $request['order_id'],
            'stock_id' => $request['stock_id']
        ]);
        
        $offer->save();

        $order = Order::find($request['order_id']);
        $order->confirmed_offer = 'yes';
        $order->save();
        
        // $order = Order::find($request->data_id);
        // return $order;
        return redirect()->route('seller.index');
    }

